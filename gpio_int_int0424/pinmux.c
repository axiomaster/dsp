void pinmux()
{
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(0) )  = 0x08888888; //GPIO09-GPIO15
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(1) )  = 0x08888888; //GPIO01-GPIO07
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(2) )  = 0x02222220; //McBSP0 管脚
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(3) )  = 0x00441111; //UART0,没有使用,SPI0 管脚,GP83,GP84
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(4) )  = 0x00222211; //UART2,I2C0,SPI0_SCS[0],SPI0_SCS[1]
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(5) )  = 0x80111111; //SPI1 管脚
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(6) )  = 0x88118888; //
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(7) )  = 0x01110011; //EMA CA2,CS3
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(8) )  = 0x11111111; //EMIFA
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(9) )  = 0x11111111; //EMIFA
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(10) ) = 0x11100000; //EMIFA
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(11) ) = 0x11111111; //EMIFA
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(12) ) = 0x11111111; //EMIFA
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(13) ) = 0x00000000; //
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(14) ) = 0x00000000; //
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(15) ) = 0x00000000; //
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(16) ) = 0x00000000; //
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(17) ) = 0x00000000; //BOOT pins
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(18) ) = 0x00000000; //
	HWREG( SOC_SYSCFG_0_REGS + SYSCFG0_PINMUX(19) ) = 0x00000000; //
}


